package PlacementPrep;

public class ASCIIcodeDisplay {

    public static void main(String[] args) {

        int acode;
        char ch1 = 150;
        System.out.println("The character with ASCII 150 is " + ch1);
        char ch2 = 'Z';
        acode = ch2;
        System.out.println("The ASCII code of Z is " + acode);
    }
}
